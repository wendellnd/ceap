
public class ListacincoCinco {
	public static void main(String args[]) {
		int a,b,size = 5;
	    for (a = size; a >= 0; a--) {
	        
		    
	    for (b = a; b < size; b++)
	        System.out.print(" ");

	    
	    for (b = 1; b <= ((a * 2) - 1); b++)
	        System.out.print("*");

	    System.out.print("\n");
	    }
	}
}
